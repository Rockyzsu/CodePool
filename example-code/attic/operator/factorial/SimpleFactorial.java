class SimpleFactorial {

    public static long factorial(long n) {
        return n < 2 ? 1 : n * factorial(n-1);
    }

    public static void main(String args[]) {
        for (long i = 1; i <= 25; i++) {
            System.out.println(i + "! = " + factorial(i));

        }
    }
}

